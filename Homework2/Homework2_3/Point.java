package ru.mirea.lab1.Homework2.Homework2_3;

public class Point {


    private int X = 0;
    private int Y = 0;

    Point(int x, int y)
    {
        X=x;
        Y=y;
    }

    public int GetX()
    {
        return X;
    }

    public int GetY()
    {
        return Y;
    }
}
